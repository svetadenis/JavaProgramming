package day16_LoopContinue;

public class NestedLoopIntro {
    public static void main(String[] args) {
        for(int i =0; i<5; i++){
            System.out.println("Wooden Spoon");
        }
        for(int i =0; i<5; i++){
            System.out.println("Wooden Spoon");
        }
        for(int i =0; i<5; i++) {
            System.out.println("Wooden Spoon");
        }
        System.out.println("____________________________________");
        for (int i = 0;i<5;i++){
            for(int j = 0;j<5;j++){
            System.out.println("Wooden Spoon");
        }}



}}
