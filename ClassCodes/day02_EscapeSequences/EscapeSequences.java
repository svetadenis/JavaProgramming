package day02_EscapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello students\nToday we hava Java class");

        System.out.println("-----------------------------");

        System.out.println("\tJava Programming Language");
        System.out.println("------------------------------");

        System.out.println("\\");

        System.out.println("_____________________________");
        System.out.println("My favorite TV show is \"The Walking Dead\"");
/*
1. \n: New Line
2. \t: paragraph space
3. \\: Back slash
4. \": double quote


 */
    }
}
