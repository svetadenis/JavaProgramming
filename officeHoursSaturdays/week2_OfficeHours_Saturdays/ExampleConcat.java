package week2_OfficeHours_Saturdays;

public class ExampleConcat {
    public static void main(String[] args) {
        char first = 'S';
        char second = 'V';
        char third = 'E';
        char forth = 'T';
        char fifth = 'A';

        System.out.println(first);
        System.out.println(second);
        System.out.println(third);
        System.out.println(forth);
        System.out.println(fifth);
        System.out.println("My name is " + first + second + third + forth + fifth);
        System.out.println(first + second +third + forth +fifth + " is my name");
        System.out.println("" + first + second +third + forth +fifth + " is my name" );

    }
}
