package Week3_OfficeHours_Saturdays;

public class DataTypes {
    public static void main(String[] args) {
        System.out.println((4+4));//int + int 8
        System.out.println(2.5+3.1);//double+double 5.6
        System.out.println(1+1.2);//int+double=>double 2.2

        float a = 4+1.5F;
        double b = 3.2F +3.4; //float+double=double
        //double is biffer then float, then double is the result

        byte c = 3;
        short d = 5;
        int result = c + d;
        //special rule: math with byte or short results int

        System.out.println(20/4);//5
        System.out.println(21.0/4);//5.25
        System.out.println(20%4);//0 remainder
        System.out.println(21%4);//1 remainder


    }
}
